// push-insert element at the last index
var a = [10, 20, 30];
console.log(a);
a.push([4, 5, 6]);//inserted at the end
console.log(a);

// pop-removes the last element of the array
a.pop();
console.log(a);

//shift-removes the element of the array which is at 0th index
a.push(34, 45, 65);
a.shift();
console.log(a);

//unshift-insert element at the 0th index of the array
a.unshift("Ram Ram");
console.log(a); 